package cc.aurora.bot.lifecycle

/**
 * 模块生命周期接口。
 *
 * 定义 Aurora 各模块的标准化生命周期回调，用于：
 * 1. 统一管理模块初始化状态
 * 2. 安全关闭时释放资源
 * 3. 集中处理模块级别的错误
 *
 * 实现类应在生命周期各阶段执行相应的初始化与清理操作。
 * 所有回调方法都有默认空实现，模块可按需覆写。
 */
interface ModuleLifecycle {

    /**
     * 模块初始化完成时调用。
     *
     * 当模块完成所有必要的初始化步骤后触发此回调。
     * 可在此方法中执行依赖其他模块已就绪的初始化操作。
     */
    fun onInitialized() {
        // 默认空实现
    }

    /**
     * 模块即将关闭时调用。
     *
     * 当系统即将终止或模块需要释放资源时触发此回调。
     * 实现类应在此方法中释放所有持有的资源（网络连接、文件句柄、协程等）。
     * 此方法应尽可能快速完成，不应执行长时间阻塞操作。
     */
    fun onShutdown() {
        // 默认空实现
    }

    /**
     * 模块发生错误时调用。
     *
     * 当模块内部发生不可恢复的错误时触发此回调。
     * 实现类可在此方法中记录错误日志、发送告警或执行降级策略。
     *
     * @param error 发生的错误异常
     */
    fun onError(error: Throwable) {
        // 默认空实现
    }
}